package javapdf2;

public class Q4EvenOrOdd {
    // Given number is even or odd
    // if else conditions
    // 2 4 6 8 10   Even number
    // 1 3 5 7 9    odd number
    public static void main(String[] args) {
        int c = 24;
        if (c % 2== 0) {
            System.out.println("Even number");
        } else {
            System.out.println("odd number");
        }
    }
}
