package javapdf2;

public class Q5ArithmeticDataTypes {
    public static void main(String[] args) {
        int S=20;
        int k=40;
        System.out.println(S+k); // 60 Addition
        System.out.println(S-k); // -20 Subtraction
        System.out.println(S*k); // 800 Multiplication
        System.out.println(S/k); // 0 Division
        System.out.println(S++); // 20 Increment operator
        System.out.println(S--); // 21 Decrement Operator
    }
}
