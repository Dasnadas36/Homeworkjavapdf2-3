package javapdf3;

public class Q4WhileLoopEvenAndOdd {
    public static void main(String[] args) {
        System.out.println("even number");
        int k=0;
        while (k<=20){
            System.out.println(+k);
            k+=2;
        }
        System.out.println("odd number");
        int i=1;
        while (i<=20){
            System.out.println(+i);
            i+=2;
        }
    }
    }


