package javapdf3;

public class Q6DoWhileLoopEvenAndOdd {
    public static void main(String[] args) {
        System.out.println("even number");
        int n = 0;
        do {
            System.out.println(n);
            n += 2;
        } while (n <= 20);

        System.out.println("odd number");
        int h = 1;
        do {
            System.out.println(h);
            h += 2;
        } while (h <= 20);
    }
    }





